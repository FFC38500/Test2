    protected void test() {
        String out = "Exercices concernés : ";
        List<String> paths;
        if (this.jCheckBox1.isSelected()) {
            paths= FindAndReplace.findStringAndReplace(FindAndReplace.exerciceExprFile, jTextField2.getText(), "xyxy", false);
            
        }else{
            paths= FindAndReplace.findStringAndReplace(FindAndReplace.exerciceFile, jTextField2.getText(), "xyxy", false);
        }
        out+=paths.size();
        
        jTextArea1.setText("");
        for (String p : paths){
            jTextArea1.append(p+"\n");
        }
        returnLabel.setText(out);
    }

