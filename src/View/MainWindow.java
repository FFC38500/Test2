    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[] split = jTextPane1.getText().split("\n");
        List<String> lines = new ArrayList<>();
        for (String s : split) {
            lines.add(s);
        }

        switch (mode) {

            case modeComposition:
                TexWriter.writeTexFile(lines);
                creationSujetView1.currentCompo=lines;
                if (TexWriter.latexToPdf()){
                    TexWriter.openPdf();
                }else{
                jTextPane1.setText("pdflatex n'a pas pu compiler le fichier latex \n \n - Essayer de corriger ce fichier ici\n - ou bien en ouvrant ce dernier avec un editeur tex pour corriger ce probl√®me");
                }

                break;

            case modeReadme:
                TexWriter.writeToFile(lines, creationSujetView1.getSelectedExercice().getPath()+"/readme.txt");
                break;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

